def convertCelsiusToKelvin(celsius):
    """
    Convert Celsius to Kelvin

    :param celsius:
    :return:
    """
    return celsius + 273.15


def convertCelsiusToFahrenheit(celsius):
    """
    Convert Celsius to Fahrenheit

    9/5 * celsius + 32

    :param celsius:
    :return:
    """
    return celsius

